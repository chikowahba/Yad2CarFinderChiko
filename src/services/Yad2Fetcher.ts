  constructor(
    baseUrl: string = 'https://www.yad2.co.il/vehicles/_next/data/mWMnB1EPqjGDEd121YWnw/cars.json',
    searchParams: Record<string, string> = {}
  ) {
    this.baseUrl = baseUrl;
    this.searchParams = searchParams;
  }

  /**
   * Fetch car listings from Yad2
   */
  public async fetchListings(): Promise<Yad2Listing[]> {
    var maximumAttempts = 20;

    for (let attempt = 1; attempt <= maximumAttempts; attempt++) {
      try {
        const userAgent = this.getRandomUserAgent();
        const proxyString = this.getRandomProxy();
        
        // Add random delay (longer for each retry)
        const delay = attempt * 1000;
        await new Promise(resolve => setTimeout(resolve, delay));
        
        // Parse proxy parts
        const [auth, hostPort] = proxyString.split('@');
        const [username, password] = auth.split(':');
        const [host, portStr] = hostPort.split(':');
        const port = parseInt(portStr, 10);
        
        console.log(`Attempt ${attempt}: Fetching via proxy ${host}:${port}`);

        const response: AxiosResponse<Yad2Response> = await axios.get(this.baseUrl, {
          params: this.searchParams,
          headers: {
            'Accept': '*/*',
            'Accept-Language': 'he-IL,he;q=0.9,en-US;q=0.8,en;q=0.7',
            'Connection': 'keep-alive',
            'User-Agent': userAgent,
            'Referer': 'https://www.yad2.co.il/vehicles/cars',
            'Cache-Control': 'no-cache',
            'Pragma': 'no-cache'
          },
          proxy: {
            host,
            port,
            auth: {
              username,
              password
            },
            protocol: 'http'
          },
          timeout: 30000
        });

        // If we reach here, the request was successful
        return this.extractListings(response.data);
      } catch (error: any) {
        // If this is the last attempt, rethrow the error
        if (attempt === maximumAttempts) {
          console.error('All proxy attempts failed');
          throw error;
        }
        
        // Otherwise, continue to the next attempt
        console.log(`Will try again with a different proxy`);
      }
    }
    
    // This should never be reached due to the throw above, but TypeScript requires a return
    return [];
  }

  private getRandomUserAgent(): string {
    return this.userAgents[Math.floor(Math.random() * this.userAgents.length)];
  }

  private getRandomProxy(): string {
    return this.proxies[Math.floor(Math.random() * this.proxies.length)];
  }

  /**
   * Extract car listings from Yad2 response
   */
  private extractListings(data: Yad2Response): Yad2Listing[] {
    try {
      // Find the query result containing car listings
      const feedData = data.pageProps?.dehydratedState?.queries?.find(query =>
        query.queryKey && query.queryKey[0] === 'feed' && query.queryKey[1] === 'vehicles'
      );

      if (!feedData?.state?.data) {
        console.log('No feed data found');
        throw new Error("got 0 results, trying with another proxy? could be captcha block");
      }

      // Extract and combine private and commercial listings
      const privateListings = feedData.state.data.private || [];
      const commercialListings = feedData.state.data.commercial || [];

      return [...privateListings, ...commercialListings];
    } catch (error) {
      console.error('Error extracting listings:', error);
      throw error;
    }
  }
